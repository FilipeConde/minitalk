#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*result;
	int		i;

	i = 0;
	result = (char *) malloc(ft_strlen(s) + 1 * sizeof(char));
	if (s[0] == '\0')
	{
		result[0] = '\0';
		return (result);
	}
	while (s[i] != '\0')
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
