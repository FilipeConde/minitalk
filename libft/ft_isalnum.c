#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (8);
	else if (c >= 48 && c <= 57)
		return (8);
	else
		return (0);
}
